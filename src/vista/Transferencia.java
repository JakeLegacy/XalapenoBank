    private void llenarRFCs(){
    try {
        cn = con.getConnection();
        st = cn.createStatement();
        String nombreSeleccionado = NombreTrans.getText();
        rs = st.executeQuery("SELECT RFC FROM Clientes WHERE Nombre != '" + nombreSeleccionado + "' AND RFC NOT IN (SELECT RFC FROM Clientes WHERE Nombre = '" + nombreSeleccionado + "')");
        
        RFCombo.removeAllItems();
        
        while (rs.next()) {
            String rfc = rs.getString("RFC");
            RFCombo.addItem(rfc);
        }
        
    } catch(Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error al cargar los RFC: "+ e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }


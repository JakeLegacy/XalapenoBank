    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
    try {
        String rfcDestino = RFCombo.getSelectedItem().toString();
        String nombreUsuario = NombreTrans.getText();
        
        // Obtener RFC del usuario actual
        String rfcUsuarioActual = "";
        Statement st = cn.createStatement();
        ResultSet rsRfcUsuario = st.executeQuery("SELECT RFC FROM Clientes WHERE Nombre = '" + nombreUsuario + "'");
        if (rsRfcUsuario.next()) {
            rfcUsuarioActual = rsRfcUsuario.getString("RFC");
        }
        
        if (rfcDestino.equals(rfcUsuarioActual)) {
            JOptionPane.showMessageDialog(this, "No puedes transferir saldo a tu misma cuenta.", "Error", JOptionPane.ERROR_MESSAGE);
            return; // Salir del método si se intenta transferir a la misma cuenta
        }
        
        // Continuar con el resto del código de transferencia si no es la misma cuenta
        
        // Obtener saldo del destinatario
        String query = "SELECT Saldo FROM Clientes WHERE RFC = '" + rfcDestino + "'";
        ResultSet rs = st.executeQuery(query);
        if (rs.next()) {
            int saldoDestino = rs.getInt("Saldo");
            int cantidadATransferir = 100; // Cantidad a transferir
            String querySaldoUsuario = "SELECT Saldo FROM Clientes WHERE Nombre = '" + nombreUsuario + "'";
            ResultSet rsSaldoUsuario = st.executeQuery(querySaldoUsuario);
            if (rsSaldoUsuario.next()) {
                int saldoUsuario = rsSaldoUsuario.getInt("Saldo");
                if (saldoUsuario >= cantidadATransferir) {
                    // Realizar la transferencia actualizando el saldo en la base de datos
                    int nuevoSaldoDestino = saldoDestino + cantidadATransferir;
                    int nuevoSaldoUsuario = saldoUsuario - cantidadATransferir;
                    String updateQueryDestino = "UPDATE Clientes SET Saldo = " + nuevoSaldoDestino + " WHERE RFC = '" + rfcDestino + "'";
                    String updateQueryUsuario = "UPDATE Clientes SET Saldo = " + nuevoSaldoUsuario + " WHERE Nombre = '" + nombreUsuario + "'";
                    st.executeUpdate(updateQueryDestino);
                    st.executeUpdate(updateQueryUsuario);
                    JOptionPane.showMessageDialog(this, "Transferencia realizada correctamente, su código: "+codigo);

                    // Definir cantidadRetirada y nuevoSaldo aquí
                    int cantidadRetirada = cantidadATransferir;
                    int nuevoSaldo = nuevoSaldoUsuario;

                    // Generar PDF
                    String rfcSeleccionado = RFCombo.getSelectedItem().toString();
                    generarPDF(cantidadRetirada, nuevoSaldo, rfcSeleccionado);
                } else {
                    JOptionPane.showMessageDialog(this, "No tienes suficiente saldo para realizar la transferencia.");
                }
            }
        }
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error al realizar la transferencia: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

